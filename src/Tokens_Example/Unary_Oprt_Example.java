package Tokens_Example;

public class Unary_Oprt_Example {

	public static void main(String[] args) 
	{

		int a = 0;
		int b =0;
		//PreIncrement and PostIncrement
		System.out.println("Orginal value a: "+ a);
		System.out.println("PreIncrement value a: "+ ++a);
		System.out.println("PostIncrement value a: "+ a++);
		System.out.println("Orginal value a: "+ a );
		
		System.out.println("$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$$ " );

		//PreDecrement and PostDecrement
		System.out.println("Orginal value b: "+ b);
		System.out.println("PreDecrement value b: "+ --b);
		System.out.println("PostDecrement value b: "+ b--);
		System.out.println("Orginal value b: "+ b );

	}

}
